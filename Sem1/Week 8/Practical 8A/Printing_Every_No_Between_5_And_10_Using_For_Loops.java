public class Printing_Every_No_Between_5_And_10_Using_For_Loops
{
   public static void main(String [] args)
   {
      // print Every No between 5 and 10
      for(int i=5; i<=10&&i>=5; i++)
      {
         System.out.println(i);
      }
   }
}