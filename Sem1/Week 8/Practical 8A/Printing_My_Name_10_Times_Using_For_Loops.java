public class Printing_My_Name_10_Times_Using_For_Loops
{
   public static void main(String [] args)
   {
      //prints my name 10 times
      for(int i=1; i<=10; i++)
      {
         System.out.println("John Gallagher");
      }// end for
   }// end main method
}// end class