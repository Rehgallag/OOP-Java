public class Printing_1_To_10_In_Reverse_Order_Using_For_Loops
{
   public static void main(String [] args)
   {
      // print 1 to 10 in reverse order
      for(int i=10; i>=1; i--)
      {
         System.out.println(i);
      }
   }
}