import java.util.Scanner;

public class Printing_25_Asterisks_Using_Nested_Loops {
   
   public static void main(String[] args) {
      
      for(int i=1;i<=5;i++) 
      {
         for(int o=1;o<=5;o++)
         {
            System.out.print("*");
         }
         System.out.println();
      }  
   }
}
