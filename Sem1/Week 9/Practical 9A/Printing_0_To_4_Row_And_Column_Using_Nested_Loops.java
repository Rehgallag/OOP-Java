import java.util.Scanner;

public class Printing_0_To_4_Row_And_Column_Using_Nested_Loops
{
   public static void main(String[] args)
   {
      for(int i=1;i<=1;i++)
      {
         for(int o=1;o<=3;o++)
         {
            System.out.println("0 1 2 3 4");
         }
      }
   }
}