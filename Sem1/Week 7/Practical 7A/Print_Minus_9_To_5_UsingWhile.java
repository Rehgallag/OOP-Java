public class Print_Minus_9_To_5_UsingWhile
{
	public static void main(String args[] )
	{
		//loop counter initialisation
		int i=-9;

		//print statement
		System.out.println("Output is : ");

		//loop to print -9 to 5.
		while(i>=-9 && i<=5)	
		{
		   System.out.println(i);
			i++;
         
		}
	}
}