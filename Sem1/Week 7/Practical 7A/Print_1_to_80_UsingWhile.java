public class Print_1_to_80_UsingWhile 
{
   public static void main(String args[] )
   {
      //loop counter initialisation
      int i=1;

      //print statement
      System.out.println("Output is : ");

      //loop to print 1 to 80
      while(i<=80)
      {
         System.out.println(i);
         i++;
      }
   }
}