public class Print_67_Asterisks_UsingWhile
{
	public static void main(String args[] )
	{
		//loop counter initialisation
		int i=1;

		//print statement
		System.out.println("Output is : ");

		//loop to print 67 asterisks
		while(i<=67)	
		{
		   System.out.println("*");
			i++;
         
		}
	}
}