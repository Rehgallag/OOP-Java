public class Song 
{
   private String artist;
   private String title;
   private int year;
   private int durationInSecs;
	
   public String getTitle()
   {
      return title;
   }
   public String getArtist()
   {
      return artist;
   }

   public String artistIn()
   {
      return artist;
   }
	
   public int getYear()
   {
      return year;
   }
	
   public int getTimeInSeconds()
   {
      return durationInSecs;
   }
	
}